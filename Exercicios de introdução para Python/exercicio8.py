#COMPUTAR UM SEGUNDO VETOR QUE TERÁ O RESULTADO DA MULTIPLICAÇAO POR ESCALAR INTEIRO 5
VET1=[5,8,9,13,11]
VET2=[]
i=0
while i < len(VET1):
    VET2.append(VET1[i]*5)
    i+=1
print(VET1)
print(VET2)

